    private void setupForm() {
        // Set current date
        txtCreateDate.setText(new SimpleDateFormat("dd/MM/yyyy").format(new Date()));

        // Setup table
        setupTable();

        // Add empty state message
        if (tableProducts.getRowCount() == 0) {
            showEmptyTableMessage();
        }
    }

    private void setupTable() {
        TableUtils.applyDefaultStyle(tableProducts);
    }

    private void showEmptyTableMessage() {
        JLabel emptyLabel = new JLabel("Không có dữ liệu", SwingConstants.CENTER);
        emptyLabel.setFont(new Font("Segoe UI", Font.ITALIC, 14));
        emptyLabel.setForeground(Color.GRAY);

        JPanel emptyPanel = new JPanel(new BorderLayout());
        emptyPanel.setBackground(Color.WHITE);
        emptyPanel.add(emptyLabel, BorderLayout.CENTER);

        scrollPaneProducts.setViewportView(emptyPanel);
    }

    public void toggleEmptyTableMessage(boolean show) {
        if (show) {
            showEmptyTableMessage();
        } else {
            scrollPaneProducts.setViewportView(tableProducts);
        }
    }

