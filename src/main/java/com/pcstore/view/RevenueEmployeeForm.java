    // Thêm phương thức để cập nhật dữ liệu cho panel top
    /**
     * Cập nhật dữ liệu cho panel top nhân viên
     * 
     * @param topEmployees Danh sách chứa thông tin nhân viên hàng đầu (cần ít nhất 3 phần tử)
     */
    public void updateTopEmployeesPanel(List<Map<String, Object>> topEmployees) {
        if (topEmployees == null || topEmployees.isEmpty()) {
            for (int i = 0; i < Math.min(3, topEmployeeNames.length); i++) {
                topEmployeeNames[i].setText(bundle.getString("revenueEmployee.default.employee"));
                topEmployeeRevenues[i].setText(bundle.getString("revenueEmployee.revenue.zero"));
            }
            return;
        }
        
        for (int i = 0; i < Math.min(topEmployees.size(), topEmployeeNames.length); i++) {
            Map<String, Object> employee = topEmployees.get(i);
            
            String employeeName = (String) employee.get("employeeName");
            BigDecimal revenue = (BigDecimal) employee.get("revenue");

            String revenueFormatted = com.pcstore.utils.LocaleManager.getInstance()
                    .getCurrencyFormatter()
                    .format(revenue);
            
            topEmployeeNames[i].setText(employeeName);
            topEmployeeRevenues[i].setText(revenueFormatted);

            topEmployeePanels[i].setVisible(true);
        }
        
        for (int i = topEmployees.size(); i < topEmployeeNames.length; i++) {
            topEmployeeNames[i].setText(bundle.getString("revenueEmployee.default.employee"));
            topEmployeeRevenues[i].setText(bundle.getString("revenueEmployee.default.revenue"));
        }
    }

